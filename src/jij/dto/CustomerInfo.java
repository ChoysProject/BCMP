package jij.dto;

public class CustomerInfo {
	private String cust_id;
	private String cust_pwd;
	private String cust_name;
	private String cust_addr;
	private String cust_gender;
	private String cust_birth;
	private String cust_email;
	private String cust_phone;
	private String cust_rating;
	private int cust_point;
	private String cust_coupon;
	public String getCust_id() {
		return cust_id;
	}
	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
	public String getCust_pwd() {
		return cust_pwd;
	}
	public void setCust_pwd(String cust_pwd) {
		this.cust_pwd = cust_pwd;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public String getCust_addr() {
		return cust_addr;
	}
	public void setCust_addr(String cust_addr) {
		this.cust_addr = cust_addr;
	}
	public String getCust_gender() {
		return cust_gender;
	}
	public void setCust_gender(String cust_gender) {
		this.cust_gender = cust_gender;
	}
	public String getCust_birth() {
		return cust_birth;
	}
	public void setCust_birth(String cust_birth) {
		this.cust_birth = cust_birth;
	}
	public String getCust_email() {
		return cust_email;
	}
	public void setCust_email(String cust_email) {
		this.cust_email = cust_email;
	}
	public String getCust_phone() {
		return cust_phone;
	}
	public void setCust_phone(String cust_phone) {
		this.cust_phone = cust_phone;
	}
	public String getCust_rating() {
		return cust_rating;
	}
	public void setCust_rating(String cust_rating) {
		this.cust_rating = cust_rating;
	}
	public int getCust_point() {
		return cust_point;
	}
	public void setCust_point(int cust_point) {
		this.cust_point = cust_point;
	}
	public String getCust_coupon() {
		return cust_coupon;
	}
	public void setCust_coupon(String cust_coupon) {
		this.cust_coupon = cust_coupon;
	}
}
