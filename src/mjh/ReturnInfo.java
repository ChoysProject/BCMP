package mjh;

public class ReturnInfo {
	private int return_number;
	private String cust_id;
	private String return_method;
	private String return_date;
	private String return_reason;
	public int getReturn_number() {
		return return_number;
	}
	public void setReturn_number(int return_number) {
		this.return_number = return_number;
	}
	public String getCust_id() {
		return cust_id;
	}
	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
	public String getReturn_method() {
		return return_method;
	}
	public void setReturn_method(String return_method) {
		this.return_method = return_method;
	}
	public String getReturn_date() {
		return return_date;
	}
	public void setReturn_date(String return_date) {
		this.return_date = return_date;
	}
	public String getReturn_reason() {
		return return_reason;
	}
	public void setReturn_reason(String return_reason) {
		this.return_reason = return_reason;
	}
	
	
}
